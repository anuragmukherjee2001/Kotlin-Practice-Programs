//variables in kotlin

fun main() {
//    var can be changed

    var language = "kotlin"
    println(language)

//    val cannot be changed

    val language2 = "Python"
    println(language2)

    var salary = 3000
    println(salary)

//    Typecasting to int

    var salary2:Int = 30000
    println(salary2)

//    Typecasting to string

    var name: String = "Anurag"
    println(name)

//    Typecasting to double

    var rate_percentage:Double = 3.12
    print(rate_percentage)

}
