//This code snippet is all about typecasting

fun main(){

    val num1 = 13
    val num1_long = num1.toLong()
    println(num1_long)

    var num2:Long = 12364
    var num2_int = num2.toInt()
    println(num2_int)

    var str1:String = "46"
    var str1_int = str1.toInt()
    println(str1_int)

//    we can also add to str1_int
    println(str1_int + 36)



}